			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`meishitechan`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='meishibianhao'){
					this.ruleForm.meishibianhao = obj[o];
					this.ro.meishibianhao = true;
					continue;
					}
					if(o=='meishimingcheng'){
					this.ruleForm.meishimingcheng = obj[o];
					this.ro.meishimingcheng = true;
					continue;
					}
					if(o=='meishileixing'){
					this.ruleForm.meishileixing = obj[o];
					this.ro.meishileixing = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='chandi'){
